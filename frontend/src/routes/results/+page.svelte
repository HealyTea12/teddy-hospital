<script lang="ts">
    import { onMount } from 'svelte';

  let data = new Map<number, string[]>(); // 64-bit encoded
  let loading = true;
  let error = null;

  async function fetchData() {
    console.log('Fetching data...');
    try {      
      const res = await fetch('http://localhost:8000/results', {
          method: "GET",
          headers: {
              'Content-Type': 'application/json',
              'Accept': 'application/json'
          }
      });
      
      if (!res.ok) {
        throw error = new Error(`HTTP error! Status: ${res.status}, ${res.statusText}`);
      }
      
      const jsonData = await res.json();
      console.log('Raw JSON data:', jsonData);
      for (const item of jsonData.results) {
        if (data.has(item.job_id)) {
            data.get(item.job_id).push(item.results);
            } else {
            data.set(item.job_id, item.results);
        }
      }
      data = data; // Trigger reactivity explicitly
      
      console.log('Data fetched successfully:', data);
    } catch (e) {
      console.error('Error fetching data:', e);
      let error = e.message;
    } finally {
      loading = false;
    }
  }
   onMount(() => {
    fetchData();
    const interval = setInterval(fetchData, 5000); // Poll every 5s
    return () => clearInterval(interval);
  });

  async function confirmJob(imageId: number, confirm: boolean) {
    try {
      const res = await fetch(`http://localhost:8000/confirm?image_id=${imageId}&confirm=${confirm}`, {
        method: 'GET',
        headers: {
          'Accept': 'application/json'
        }
      });
      
      if (!res.ok) {
        throw new Error(`Failed to ${confirm ? 'approve' : 'reject'} job: ${res.statusText}`);
      }
      
      // Refresh the data after confirming
      fetchData();
      
      alert(`Job ${imageId} ${confirm ? 'approved' : 'rejected'} successfully!`);
    } catch (e) {
      console.error('Error confirming job:', e);
      alert(`Error: ${e.message}`);
    }
  }

 
</script>

<svelte:head>
	<title>Results</title>
	<meta name="description" content="Results page" />
</svelte:head>

<div class="text-column">
    <h1>Results</h1>
    <p>In this page you can see and approve the results generated by the AI.</p>
    
    <!-- Debug section to see the raw data -->
    <details>
        <summary>Debug Data (click to expand)</summary>
        <pre>{JSON.stringify(data, null, 2)}</pre>
    </details>
    
    {#if error}
        <div class="error">
            Error loading results: {error}
        </div>
    {/if}
    
    <table>
        <thead>
            <tr>
                <th>ID</th>
                <th>Result</th>
            </tr>
        </thead>
        <tbody>
            {#if loading}
                <tr>
                    <td colspan="2">Loading...</td>
                </tr>
            {:else if data.size === 0}
                <tr>
                    <td colspan="2">No results yet</td>
                </tr>
            {:else}
                {#each data as results, job_id (job_id)}
                    <tr>
                        <td>{results[0]}</td>
                        <td>
                        {#each results[1] as image, index}
                        <img src={`data:image/png;base64,${image}`} alt="result ${index}" />
                        {/each}
                        </td>
                    </tr>
                {/each}
            {/if}
        </tbody>
    </table>
</div>

